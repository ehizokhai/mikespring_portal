    {
        $classroom = new Classroom;
        $classroom->name = $request->title;
        $classroom->alias = $request->codeName;
        $classroom->schoolCategory = $request->category;
        if($classroom->save()){
            return response()->json([
                'data' => $classroom,
                'title' => 'success'
            ]);
        }
    }

